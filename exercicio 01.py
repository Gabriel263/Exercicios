A = float(input("Digite o valor de A: "))
B = float(input("Digite o valor de B: "))
C = float(input("Digite o valor de C: "))

if A + B < C:
    print("A soma de " + str(A) + " + " + str(B) + " é menor que " + str(C) + ".")
else:
    print("A soma de " + str(A) + " + " + str(B) + " não é menor que " + str(C) + ".")
